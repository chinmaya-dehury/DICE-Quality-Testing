function MAP=map2_fit(e1,e2,e3,G2)
if G2>=0
    MAP=mmpp2_fit3(e1,e2,e3,G2);
else
    g=-G2;
    b=(24*e1^3*e3-36*e1^4*e2+9*e2^3+2*e3^2+18*e1^2*e2^2+18*e1^2*e2^2*g-24*e1*e2*e3-36*e1^4*e2*g+12*e1^3*e3*g-6*e1*e2*e3*g)/(12*e3*e1^3-18*e1^2*e2^2-6*e3*e1*e2+9*e2^3) + ((e3-3*e1*e2-sqrt(24*e1^3*e3-27*e1^2*e2^2-18*e1*e2*e3+18*e2^3+e3^2))*(e3-6*e1*e2+6*e1^3*g+6*e1^3-3*e1*e2*g))/((3*e2 - 6*e1^2)*(2*e1*e3-3*e2^2));
    l1=(2*e3-6*e1*e2)/(2*e1*e3-3*e2^2) - (e3 - 3*e1*e2 - sqrt(24*e1^3*e3 - 27*e1^2*e2^2 - 18*e1*e2*e3 + 18*e2^3 + e3^2))/(2*e1*e3-3*e2^2);
    l2=(e3 - 3*e1*e2 - sqrt(24*e1^3*e3 - 27*e1^2*e2^2 - 18*e1*e2*e3 + 18*e2^3 + e3^2))/(2*e1*e3-3*e2^2);
    a=G2/(-b);
    MAP{1}=[-l1,(1-a)*l1;0,-l2];
    MAP{2}=[0,a*l1;b*l2,(1-b)*l2];
end
end